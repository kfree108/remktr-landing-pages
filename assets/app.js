function hydrateVideos() {
  const videos = [...document.querySelectorAll("video[data-remktr-video]")];
  if (!videos.length) return;

  const MAX_CONCURRENT = 2;
  let active = 0;
  const queue = [];

  const tryPlay = video => {
    video.muted = true;
    video.defaultMuted = true;
    video.playsInline = true;
    video.loop = true;
    const playPromise = video.play();
    if (playPromise && typeof playPromise.catch === "function") {
      playPromise.catch(() => {});
    }
  };

  const release = video => {
    delete video.dataset.loading;
    active = Math.max(0, active - 1);
    const next = queue.shift();
    if (next) startLoad(next);
  };

  const startLoad = video => {
    if (video.dataset.loaded) { tryPlay(video); return; }
    if (video.dataset.loading) return;
    if (active >= MAX_CONCURRENT) {
      if (!queue.includes(video)) queue.push(video);
      return;
    }
    active += 1;
    video.dataset.loading = "1";
    video.addEventListener("loadeddata", () => {
      video.dataset.loaded = "1";
      release(video);
      tryPlay(video);
    }, { once: true });
    video.addEventListener("error", () => {
      const tries = Number(video.dataset.tries || 0);
      release(video);
      if (tries < 3) {
        video.dataset.tries = String(tries + 1);
        setTimeout(() => {
          video.removeAttribute("src");
          startLoad(video);
        }, 1500 * (tries + 1));
      }
    }, { once: true });
    video.src = video.dataset.src;
    video.load();
  };

  const visibleEnough = video => {
    const rect = video.getBoundingClientRect();
    return rect.bottom > -400 && rect.top < window.innerHeight + 400;
  };

  videos.forEach(video => {
    if (visibleEnough(video)) startLoad(video);
  });

  if ("IntersectionObserver" in window) {
    const observer = new IntersectionObserver(entries => {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          startLoad(entry.target);
        } else if (entry.target.dataset.loaded) {
          entry.target.pause();
        }
      });
    }, { rootMargin: "600px 0px", threshold: 0 });
    videos.forEach(video => observer.observe(video));
  }

  ["pointerdown", "touchstart", "scroll"].forEach(eventName => {
    window.addEventListener(eventName, () => {
      videos.forEach(video => {
        if (visibleEnough(video)) startLoad(video);
      });
    }, { once: true, passive: true });
  });
}

